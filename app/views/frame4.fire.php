    <h2 class="blockArticle">Відповідність формату</h2>
    <div>
        <p>“Зовнішній вигляд” власного висловлювання оцінюється 2 балами.</p>
        <p>Ідеальний твір має виглядати так:</p>
    </div>
    <div>
        <p>Привітання:<p>Dear/Hi/Hello Tom,</p></p>
        <hr>
        <p>Абзац №1:<p>вступ (opening remarks);</p></p>
        <hr>
        <p>Абзац №2:<p>перша умова;</p></p>
        <hr>
        <p>Абзац №3:<p>друга умова;</p></p>
        <hr>
        <p>Абзац №4:<p>третя умова;</p></p>
        <hr>
        <p>Абзац №5:<p>кінцівка (closing remarks);</p></p>
        <hr>
        <p>Прощання:<p>Best wishes,/Yours,/Lots of love,... </p></p>
        <hr>
        <p>Підпис:<p>Amanda/Amanda sMITH</p></p>
    </div>
    <p><b>Ще важливий момент. </b>Знімається бал за цей критерій, якщо у вступі дослівно переписано умову письмового завдання. <b>Тож не можна брати більше як 3 слова підряд із завдання!</b></p>
    <p>Погляньмо, як би виглядала структура з приблизними вступом і кінцівкою:</p>
    <div>
        <p>Dear Tom, </p>
        <hr>
        <p>Thank you for your last letter. How are you now? Sorry, I've taken so long to put a pen to paрer, but I've been really busy. I thought I'd write and let you know about my new house.</p>
        <hr>
        <p>абзац №2: Firstly, + перша умова</p>
        <hr>
        <p>абзац №3: Moreover, + друга умова</p>
        <hr>
        <p>абзац №4: Finally, + третя умова</p>
        <hr>
        <p>Well, that's all my news for now. I'd better go and do some work. Give my regards to your parents. I'd be glad to hear from you soon.</p>
        <hr>
        <p>Прощання:<p>Best wishes,/Yours,/Lots of love,... </p></p>
        <hr>
        <p>Best wishes,<p>Mary</p></p>
