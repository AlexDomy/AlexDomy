<div class="thirdBlock">
    <h2 class="blockArticle">Логіка, зв’язність</h2>
    <div>
        <p>2 бали ви отримаєте за так звані <b>зв'язкові слова.</b> Це різні сполучники та вставні слова. Лише and, but тa also не гарантують 2 балів. Добре, що вони є, та їх буде мало. А от якщо до них додати ще besides, firstly, moreover, all in all та however тo вийде чаpівна суміш, за яку можна отримати максимум.</p>
        <p>Втім, слід знати міру. Траплялись такі твори, в яких кожне речення починалось з таких слів. Вони не завжди були доречними. Крім цього, така їх навала заважала розуміти написане. В такому разі можуть зняти бал.</p>
    </div>
    <div class="delimiter"></div>
    <h2 class="blockArticle">Використання граматики</h2>
    <div>
        <p>Граматичні помилки включають в себе неправильне використання граматичних конструкцій + неправильне написання слів. Всі помилки цього типу поділяються на дрібні та грубі. Зробивши 8 дрібних помилок ще можна отримати 2 бали. А от 1 груба одразу грозить прощанням з 1 балом. Ось які помилки, згідно з УЦОЯО, Вважаються грубими:</p>
        <ul>
            <li>вживання числа і особи в дієслівних часових формах (ех.: she speak - spреaks; we has-һave written) </li>
            <li>порушення порядку слів у реченні (ех.: / very like my house. — like my house very much.) </li>
            <li>вживання інфінітива (ех.: lwant tell — I want to telli; she should to comе — she should come.) </li>
            <li>вживання незлічуваних іменників (ех.: My news are -is; She gave me sоте advices->advice)</li>
        </ul>
    </div>
    <div class="delimiter"></div>
    <h2 class="blockArticle">Використання лексики</h2>
    <div>
        <p>Допускається 3 лексичні помилки, проте неправильне написання слів не відноситься до лексичних помилок.</p>
        <p><b>Лексичними вважаються:</b> вживання слів в непритаманному їм значенні та неправильне вживання фразових дієслів. Однак, якщо в роботі немає 3-х лексичних помилок, а лише 2 - це ще не означає, що учень гарантовано отримає 2 бали за цей критерій.</p>
    </div>
    <div class="delimiter"></div>
    <h2 class="blockArticle">Використання граматики</h2>
    <div>
        <p>В першу чергу, всім знати <b>критерії оцінювання</b>!</p>
        <div>
            <p>а1, а2, а3 3D 6 балів-розкриття умов, зазначених у завданні №43</p>
            <hr>
            <p>b1 = 2 бали -логіка викладу та зв'язність тексту</p>
            <hr>
            <p>b2 = 2 бали— відповідність формату</p>
            <hr>
            <p>с = 2 бали — використання лексики</p>
            <hr>
            <p>d = 2 бали - використання граматики</p>
            <hr>
            <p>Разом - заповітні 14 балів.</p>
        </div>
        <p><b>Розкриття умов</b>. Все досить просто- треба писати по темі, не зачіпати нічого, про що не запитують. Зайва інформація не береться до уваги під час оцінювання умов, проте помилки в ній-дуже навіть повпливають на бали за лексику або граматику. Обов'язково потрібно написати про кожну умову, не лише згадати ї (або аж ніяк не переписати)./p>
        <p>Окрім цього, варто нагадати, що коли хтось отримує «О» балів за критерії а1, а2 та аз (умови), або за с (лексику) чи d (граматику) тоді весь твір оцінюється в «О» балів.</p>
        <p>Впевнено можна сказати, що ці підказки та інформація допоможуть уcім розібратись зі структурою твору та процедурою перевірки, а вчителям- більш ефективно та безпомилково розказувати учням, як можна отримати бажані 14 балів :)</p>
